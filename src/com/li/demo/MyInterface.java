package com.li.demo;
/*
* 接口关键字interface
* */
public interface MyInterface {
    int age=10;
    public static int num=10;//静态共享变量
    public final int NUMBER=11;//常量 所有字母都必须大写
    public static final int JOB_NUMBER=100;//静态常量，常量必须直接初始化并且不能直接1更改

    //final 最终的//
    //final修饰变量 变量变常量
    //final修饰类不能被继承

}
